export const SCHEMA = [
  `CREATE TABLE IF NOT EXISTS barbers (
                                        id           INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
                                        name         TEXT NOT NULL,
                                        photo        TEXT NOT NULL,
                                        title_pl     TEXT NOT NULL,
                                        title_en     TEXT NOT NULL,
                                        sort_order   INTEGER NOT NULL DEFAULT 0,
                                        active       INTEGER NOT NULL DEFAULT 1,
                                        slug         TEXT,
                                        bio_pl       TEXT,
                                        bio_en       TEXT,
                                        long_bio_pl  TEXT,
                                        long_bio_en  TEXT,
                                        delay        INTEGER NOT NULL DEFAULT 0
   )`,
  `CREATE INDEX IF NOT EXISTS idx_barbers_slug ON barbers(slug)`,

  `CREATE TABLE IF NOT EXISTS services (
                                         id             TEXT PRIMARY KEY,
                                         name_pl        TEXT NOT NULL,
                                         name_en        TEXT NOT NULL,
                                         desc_pl        TEXT NOT NULL,
                                         desc_en        TEXT NOT NULL,
                                         duration_min   INTEGER NOT NULL,
                                         duration_label TEXT NOT NULL,
                                         price_pln      INTEGER NOT NULL,
                                         delay          INTEGER NOT NULL DEFAULT 0,
                                         sort_order     INTEGER NOT NULL,
                                         active         INTEGER NOT NULL DEFAULT 1,
                                         category       TEXT NOT NULL DEFAULT 'hair'
                                         CHECK (category IN ('hair','beard','combo'))
    )`,

  `CREATE TABLE IF NOT EXISTS bookings (
                                         id           INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
                                         barber_id    INTEGER NOT NULL REFERENCES barbers(id),
    service_id   TEXT NOT NULL,
    duration_min INTEGER NOT NULL,
    date         TEXT NOT NULL,
    slot         TEXT NOT NULL,
    name         TEXT NOT NULL,
    phone        TEXT NOT NULL,
    status       TEXT NOT NULL DEFAULT 'pending'
    CHECK (status IN ('pending','confirmed','cancelled')),
    created_at   TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (barber_id, date, slot)
    )`,
  `CREATE INDEX IF NOT EXISTS idx_bookings_lookup ON bookings(barber_id, date)`,
  `ALTER TABLE bookings ADD COLUMN IF NOT EXISTS is_block BOOLEAN NOT NULL DEFAULT false`,

  `CREATE TABLE IF NOT EXISTS service_barbers (
                                                service_id TEXT    NOT NULL REFERENCES services(id) ON DELETE CASCADE,
    barber_id  INTEGER NOT NULL REFERENCES barbers(id)  ON DELETE CASCADE,
    PRIMARY KEY (service_id, barber_id)
    )`,

  `CREATE TABLE IF NOT EXISTS app_meta (
                                         key        TEXT PRIMARY KEY,
                                         value      TEXT NOT NULL,
                                         applied_at TIMESTAMPTZ NOT NULL DEFAULT now()
    )`,

  `CREATE TABLE IF NOT EXISTS barber_tags (
                                            barber_id  INTEGER NOT NULL REFERENCES barbers(id) ON DELETE CASCADE,
    tag_pl     TEXT NOT NULL,
    tag_en     TEXT NOT NULL,
    sort_order INTEGER NOT NULL DEFAULT 0,
    PRIMARY KEY (barber_id, tag_en)
    )`,
];

export async function initSchema(runner) {
  for (const sql of SCHEMA) {
    await runner.query(sql);
  }
}

export async function ping(runner) {
  await runner.query('SELECT 1');
}
